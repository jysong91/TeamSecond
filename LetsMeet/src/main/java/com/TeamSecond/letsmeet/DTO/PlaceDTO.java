package com.TeamSecond.letsmeet.DTO;

public class PlaceDTO {
	private String id;
	private String placeName;
	private String placeAddr;
	private String outLine;
	private String tel;
	private String placeWriteDate;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPlaceWriteDate() {
		return placeWriteDate;
	}
	public void setPlaceWriteDate(String placeWriteDate) {
		this.placeWriteDate = placeWriteDate;
	}
	public String getPlaceName() {
		return placeName;
	}
	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}
	public String getPlaceAddr() {
		return placeAddr;
	}
	public void setPlaceAddr(String placeAddr) {
		this.placeAddr = placeAddr;
	}
	public String getOutLine() {
		return outLine;
	}
	public void setOutLine(String outLine) {
		this.outLine = outLine;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
}
